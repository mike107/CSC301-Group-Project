
public class Attendance {
	
	private Object courseID;
	private Object studentEmail;
	private Object sessionID;
	public Attendance() {
	}
	public Object getCourseID() {
		return this.courseID;
	}
	public void setCourseID(Object newCourseID) {
		this.courseID = newCourseID;
	}
	public Object getStudentEmail() {
		return this.studentEmail;
	}
	public void setStudentEmail(Object newStudentEmail) {
		this.studentEmail = newStudentEmail;
	}
	public Object getSessionID() {
		return this.sessionID;
		
	}
	
	public void setSessionID(Object newSessionID) {
		this.sessionID = newSessionID;
		
	}
}
